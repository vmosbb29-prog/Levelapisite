def get_level_progress(level: int, current_exp: int) -> dict:
    entry = EXP_TABLE.get(level)
    if not entry:
        return {
            "base_exp": 0, "next_level_exp": 0,
            "completed_exp": 0, "remaining_exp": 0,
            "progress_pct": 0.0, "max_level": False,
        }
    base_exp, next_exp = entry
    if next_exp is None:
        return {
            "base_exp": base_exp, "next_level_exp": None,
            "completed_exp": 0, "remaining_exp": 0,
            "progress_pct": 100.0, "max_level": True,
        }
    completed = max(0, current_exp - base_exp)
    remaining = max(0, next_exp - completed)
    pct = round(min(completed / next_exp * 100, 100.0), 1) if next_exp > 0 else 100.0
    return {
        "base_exp":      base_exp,
        "next_level_exp": next_exp,
        "completed_exp": completed,
        "remaining_exp": remaining,
        "progress_pct":  pct,
        "max_level":     False,
    }
